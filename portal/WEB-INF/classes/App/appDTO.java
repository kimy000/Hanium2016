package App;

public class appDTO {
	private String appInfoID;
	private String appUday;
	private String appUVersion;
	private String appUInfo;
	private String developername;
	public String getAppInfoID() {
		return appInfoID;
	}
	public void setAppInfoID(String appInfoID) {
		this.appInfoID = appInfoID;
	}
	public String getAppUday() {
		return appUday;
	}
	public void setAppUday(String appUday) {
		this.appUday = appUday;
	}
	public String getAppUVersion() {
		return appUVersion;
	}
	public void setAppUVersion(String appUVersion) {
		this.appUVersion = appUVersion;
	}
	public String getAppUInfo() {
		return appUInfo;
	}
	public void setAppUInfo(String appUInfo) {
		this.appUInfo = appUInfo;
	}
	public String getDevelopername() {
		return developername;
	}
	public void setDevelopername(String developername) {
		this.developername = developername;
	}
	
}
